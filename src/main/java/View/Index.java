    private void RetirarDinero(){
         if(DineroSeleccionado!=0){
           boolean error = this.MC.Sacar(DineroSeleccionado);
           if(error) return;
           JOptionPane.showMessageDialog(null, "Se Retiraron Correctamente $"+DineroSeleccionado+" Pesos");
           LimpiarDinero();
       }else{
           JOptionPane.showMessageDialog(null, "Por Favor Seleccione Primero Un Monto A Agregar");
       }
    }
    
    private void GuardarDinero(){
        if(!MonedasSeleccionadas.isEmpty()){
           this.MC.Adicionar(MonedasSeleccionadas);
           JOptionPane.showMessageDialog(null, "Se Agregaron Correctamente $"+DineroSeleccionado+" Pesos");
           LimpiarDinero();
       }else{
           JOptionPane.showMessageDialog(null, "Por Favor Seleccione Primero Un Monto A Agregar");
       }
    }
    
