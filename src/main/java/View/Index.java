    private void LimpiarDinero(){
        this.MonedasSeleccionadas.clear();
        this.DineroSeleccionado=0;
        ObtenerAhorros();
        CambiarDineroSeleccionado();
    }
    
    private void ObtenerAhorros(){
        this.cantidadDeMonedas = this.MC.CantidadDinero();
        this.nMonedas.setText(String.valueOf(MC.CantidadDeMonedas()));
        this.DineroGuardado.setText(String.valueOf(this.cantidadDeMonedas));
        System.out.println(this.MC.CantidadDinero());
    }
    
    private void ObtenerCantidadesMonedas(){
        Vector ft = new Vector();
        ArrayList<Integer> cantidadDeMonedas = new ArrayList<Integer>();
        
        cantidadDeMonedas.add(this.MC.CantidadPorMoneda(new Moneda(1000)));
        cantidadDeMonedas.add(this.MC.CantidadPorMoneda(new Moneda(500)));
        cantidadDeMonedas.add(this.MC.CantidadPorMoneda(new Moneda(200)));
        cantidadDeMonedas.add(this.MC.CantidadPorMoneda(new Moneda(100)));
        cantidadDeMonedas.add(this.MC.CantidadPorMoneda(new Moneda(50)));
        
        String f1[] = {"1000",String.valueOf(cantidadDeMonedas.get(0)),String.valueOf(1000*cantidadDeMonedas.get(0))};
        String f2[] = {"500",String.valueOf(cantidadDeMonedas.get(1)),String.valueOf(500*cantidadDeMonedas.get(1))};
        String f3[] = {"200",String.valueOf(cantidadDeMonedas.get(2)),String.valueOf(200*cantidadDeMonedas.get(2))};
        String f4[] = {"100",String.valueOf(cantidadDeMonedas.get(3)),String.valueOf(100*cantidadDeMonedas.get(3))};
        String f5[] = {"50",String.valueOf(cantidadDeMonedas.get(4)),String.valueOf(50*cantidadDeMonedas.get(4))};
        
        ft.add(f1);
        ft.add(f2);
        ft.add(f3);
        ft.add(f4);
        ft.add(f5);
        this.llenarTabla(ft);
    }
    
    private void llenarTabla(Vector f){
        dtm.setRowCount(0);
        for(int i=0;i<f.size();i++){
            String[] ft = (String[]) f.get(i);
            dtm.addRow(ft);
        }
    }
            
